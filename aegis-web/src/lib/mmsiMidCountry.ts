/** Resolve flag state from a 9-digit MMSI string (digits only). */
export function countryFromMmsi(mmsi: string | undefined | null): string | undefined {
  if (!mmsi) return undefined;
  const digits = String(mmsi).replace(/\D/g, "");
  if (digits.length !== 9) return undefined;
  const mid = parseInt(digits.slice(0, 3), 10);
  if (!Number.isFinite(mid)) return undefined;
  return MID_TO_COUNTRY[mid];
}
