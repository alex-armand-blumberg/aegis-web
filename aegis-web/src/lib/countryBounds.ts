/** Leaflet bounds: [[south, west], [north, east]] */
export type LatLngBoundsTuple = [[number, number], [number, number]];

export function getCountryBounds(country: string): LatLngBoundsTuple | null {
  const raw = COUNTRY_BBOX[country];
  if (!raw) return null;
  const [latMin, latMax, lonMin, lonMax] = raw;
  return [
    [latMin, lonMin],
    [latMax, lonMax],
  ];
}

export function getCountryCenter(
  country: string
): { lat: number; lon: number } | null {
  const raw = COUNTRY_BBOX[country];
  if (!raw) return null;
  return { lat: raw[4], lon: raw[5] };
}
